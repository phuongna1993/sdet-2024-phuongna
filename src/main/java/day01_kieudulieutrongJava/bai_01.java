package day01_kieudulieutrongJava;

/**
 * IN RA GIA TRI MIN - MAX CUA CAC KIEU DU LIEU DANG SO
 */


public class bai_01 {
    public static void main(String[] args) {

        // Print Min - max cua byte
        System.out.println("Gia tri min cua kieu byte la: " + Byte.MIN_VALUE);
        System.out.println("Gia tri max cua kieu byte la: " + Byte.MAX_VALUE);

        // Print Min - max cua int
        System.out.println("Gia tri min cua kieu int la: " + Integer.MIN_VALUE);
        System.out.println("Gia tri max cua kieu int la: " + Integer.MAX_VALUE);

        // Print Min - max cua long
        System.out.println("Gia tri min cua kieu long la: " + Long.MIN_VALUE);
        System.out.println("Gia tri min cua kieu long la: " + Long.MAX_VALUE);

        // Print Min - Max cua float
        System.out.println("Gia tri min cua kieu float la: " + Float.MIN_VALUE);
        System.out.println("Gia tri min cua kieu float la: " + Float.MAX_VALUE);

        // Print Min - max cua double
        System.out.println("Gia tri min cua kieu double la: " + Double.MIN_VALUE);
        System.out.println("Gia tri min cua kieu double la: " + Double.MAX_VALUE);
    }
}
