package day02_operators;

public class bai_01 {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;
        float c = (float)a/b ;
        System.out.println("Tong cua a va b la: " + (a+b));
        System.out.println("Hieu cua a va b la: " + (a-b));
        System.out.println("Tich cua a va b la: " + (a*b));
        System.out.println("Tich cua a va b la: " + c);
        System.out.println("Phan du khi b chia a: " + (b%a));
    }
}
